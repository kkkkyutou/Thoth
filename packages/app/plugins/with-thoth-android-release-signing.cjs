const { withAppBuildGradle } = require("expo/config-plugins");

const START_MARKER = "// @generated by with-thoth-android-release-signing";
const END_MARKER = "// @end generated by with-thoth-android-release-signing";

const SIGNING_BLOCK = `${START_MARKER}
def thothReleaseTask = gradle.startParameter.taskNames.any { taskName ->
    taskName.toLowerCase().contains("release")
}
def thothReleaseStorePath = System.getenv("THOTH_ANDROID_KEYSTORE_PATH")
def thothReleaseStorePassword = System.getenv("THOTH_ANDROID_KEYSTORE_PASSWORD")
def thothReleaseKeyAlias = System.getenv("THOTH_ANDROID_KEY_ALIAS")
def thothReleaseKeyPassword = System.getenv("THOTH_ANDROID_KEY_PASSWORD")
def thothReleaseSigningValues = [
    thothReleaseStorePath,
    thothReleaseStorePassword,
    thothReleaseKeyAlias,
    thothReleaseKeyPassword,
]

if (thothReleaseTask && thothReleaseSigningValues.any { value -> !value }) {
    throw new GradleException("Thoth production release signing environment is incomplete.")
}

if (thothReleaseStorePath) {
    android {
        signingConfigs {
            thothRelease {
                storeFile file(thothReleaseStorePath)
                storePassword thothReleaseStorePassword
                keyAlias thothReleaseKeyAlias
                keyPassword thothReleaseKeyPassword
            }
        }
        buildTypes {
            release {
                signingConfig signingConfigs.thothRelease
            }
        }
    }
}
${END_MARKER}`;

function applyThothAndroidReleaseSigning(contents) {
  const markerPattern = new RegExp(
    `\\n?${START_MARKER.replace(/[.*+?^${}()|[\\]\\]/g, "\\$&")}[\\s\\S]*?${END_MARKER.replace(/[.*+?^${}()|[\\]\\]/g, "\\$&")}\\n?`,
    "g",
  );
  return `${contents.replace(markerPattern, "").trimEnd()}\n\n${SIGNING_BLOCK}\n`;
}

function withThothAndroidReleaseSigning(config) {
  return withAppBuildGradle(config, (nextConfig) => {
    if (nextConfig.modResults.language !== "groovy") {
      throw new Error("Thoth Android release signing requires a Groovy app build.gradle.");
    }
    nextConfig.modResults.contents = applyThothAndroidReleaseSigning(
      nextConfig.modResults.contents,
    );
    return nextConfig;
  });
}

module.exports = withThothAndroidReleaseSigning;
module.exports.applyThothAndroidReleaseSigning = applyThothAndroidReleaseSigning;
